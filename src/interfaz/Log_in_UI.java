    private void LogInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogInButtonActionPerformed
        String id = ID.getText();
        String contra = contrasena.getText();
        boolean encontrado = false;
        String tipo = tipoDeUsuario.getSelectedItem().toString();
        System.out.println(tipo);
        if(tipo.equals("Cliente")){
            for(Cliente cliente:clientes){
                if(cliente.logIn(id, contra)){
                    Menu_Clientes_UI menuClientes = new Menu_Clientes_UI(cliente,this);
                    menuClientes.setVisible(true);
                    setVisible(false);
                    encontrado = true;
                }
            }
        }else if(tipo.equals("Soporte")){
            for(Soporte soporte:soportes){
                if(soporte.logIn(id, contra)){
                    MenuSoporteUI menuSoporte = new MenuSoporteUI(soporte,this);
                    menuSoporte.setVisible(true);
                    setVisible(false);
                    encontrado = true;
                }
            }
        }else if(tipo.equals("Admin")){
            if(admin.logIn(id,contra)){
                Menu_Admin menuAdmin = new Menu_Admin(admin,clientes,soportes,this);
                menuAdmin.setVisible(true);
                setVisible(false);
                encontrado = true;
            }
        }
        if(!encontrado)
        JOptionPane.showMessageDialog(null,"No hay ning√∫n usuario registrado, por favor intentelo de nuevo", "Error",JOptionPane.ERROR_MESSAGE); 
        
    }//GEN-LAST:event_LogInButtonActionPerformed
   
    
    
